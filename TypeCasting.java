class TypeCasting{
  public static void main(String[] args){
    byte n1=126;
    int n2=n1;
    System.out.println(n2);
    int n3=56;
    byte n4=(byte)n3;
    System.out.println(n4);
    int n5=276;
    byte n6=(byte)n5;
    System.out.println(n6);
    float n7=78.8987f;
    int n8=(int)n7;
    System.out.println(n8);}
}
