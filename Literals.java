public class Literals{
  public static void main(String[] args){
    int n1=0b1011;
    int n2=0x7E;
    double n3=70;
    char n4='s';
    int n5=100_00_000;
    System.out.println(n1);
    System.out.println(n2);
    System.out.println(n3);
    n4++;
    System.out.println(n4);
    System.out.println(n5);
  }
}
